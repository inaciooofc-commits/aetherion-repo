create policy "profiles own insert" on public.profiles for insert with check (auth.uid() = id);

create policy "characters own select" on public.characters for select using (auth.uid() = user_id);
create policy "characters own update" on public.characters for update using (auth.uid() = user_id);
create policy "characters own insert" on public.characters for insert with check (auth.uid() = user_id);

